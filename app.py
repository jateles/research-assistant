    col1, col2 = st.columns(2)

    with col1:
        st.download_button(
            label="Download as Markdown",
            data=summary,
            file_name=f"{saved_pmid}.md",
            mime="text/markdown",
            use_container_width=True,
        )

    with col2:
        st.download_button(
            label="Download as PDF",
            data=_build_pdf(saved_pmid, summary),
            file_name=f"{saved_pmid}.pdf",
            mime="application/pdf",
            use_container_width=True,
        )
